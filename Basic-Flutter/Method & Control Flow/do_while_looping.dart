void main() {
  /*
    do{
      statement yang akan dijalankan selama kondisi bernilai true
    } while (condition);
  */
  int angka = 100;
  do {
    print(angka);
    angka--;
  } while (angka >= 1);

  // bedanya dengan while, do while menjalankan codenya dlu baru melakukan conditional. Sedangkan while, melakukan conditional dlu baru menajlankan codenya
}
